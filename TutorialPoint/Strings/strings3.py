# escaping single quotes
print('It\'s a sunny day!')

# escaping double quotes
print("He said, \"Hello World\"")

# new line
print("Hello\nWorld")

# tab
print("Hello\tWorld")

# using backslashes
print("This is backslash : \\")

# octal and hexa decimal values
print("\101")
print("\x41")

# null character
print("Null character : \0")

# backspace
print("Helloo\b World")

# carriage return
print("HELLO\rhello")
